    private void tampildata() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Menu");
        tbl.addColumn("Kode");
        tbl.addColumn("Harga");
        tbl.addColumn("Jumlah");
        tbl.addColumn("Total");
        tbl.addColumn("Tunai");
        tbl.addColumn("Kembalian");
        
        tabeltransaksi.setModel(tbl); // tbl_mahasiswa sesuaikan dengan variable pada table
        try {
            java.sql.Connection conn=koneksiDB();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM koneksimdl5");
            while (rs.next()) {
                tbl.addRow( new Object[] {
                    rs.getString("Barang"),
                    rs.getString("Kode"),
                    rs.getString("Harga"),
                    rs.getString("Jumlah"),
                    rs.getString("Total"),
                    rs.getString("Tunai"),
                    rs.getString("Kembalian")
                });
                tabeltransaksi.setModel(tbl);
                tabel.setModel(tbl);
            }
        } catch (SQLException e) {
        }
    }

