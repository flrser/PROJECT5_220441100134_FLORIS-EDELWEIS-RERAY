    private void hitungtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungtotalActionPerformed
        // TODO add your handling code here:
        if (kode.getText().isEmpty() || harga.getText().isEmpty()|| jumbeli.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Silahkan isi kolom terlebih dahulu");
            return;
        }
        try{
            int jumlah1 = Integer.parseInt(jumbeli.getText());
            int harga1 = Integer.parseInt(harga.getText());
            int bayar = harga1 * jumlah1;
            totalharga.setText(Integer.toString(bayar));
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Masukkan angka dengan benar");
        }
        
       
    }//GEN-LAST:event_hitungtotalActionPerformed

